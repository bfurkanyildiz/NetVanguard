// в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ

fn validate_target(target: &str) -> Result<(), String> {
    if target.is_empty() {
        return Err("Hedef adresi boЕџ olamaz!".into());
    }
    for ch in [';', '&', '|', '`', '$', '\n', '\r', '(', ')', '{', '}'] {
        if target.contains(ch) {
            return Err(format!("GeГ§ersiz karakter: '{}'", ch));
        }
    }
    Ok(())
}

// в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
//  KOMUT Г‡ALIЕћTIRICI
// в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ

fn run_command(program: &str, args: &[&str]) -> (bool, String) {
    let mut cmd = Command::new("cmd");
    cmd.arg("/C").arg(program).args(args);

    match cmd.output() {
        Ok(output) => {
            let stdout = String::from_utf8_lossy(&output.stdout).to_string();
            let stderr = String::from_utf8_lossy(&output.stderr).to_string();
            let combined = if stderr.is_empty() { stdout } else { format!("{}\n{}", stdout, stderr) };
            
            if !output.status.success() {
                (false, format!("Hata: Sistem komutu yГјrГјtГјlemedi\n{}", combined))
            } else {
                (true, combined)
            }
        }
        Err(e) => (false, format!("Hata: Sistem komutu yГјrГјtГјlemedi\nDetay: {}\n'{}' programД± sisteminizde kurulu mu?", e, program)),
    }
}

// в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
//  ANA API HANDLER
// в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ

async fn handle_scan(Json(body): Json<ScanRequest>) -> Json<ScanResponse> {
    let mut target = body.target.trim().to_string();

    // в”Ђв”Ђ AkД±llД± AДџ KeЕџfi Override в”Ђв”Ђ
    if body.net_discover {
        target = "192.168.1.0/24".to_string();
    }

    if let Err(msg) = validate_target(&target) {
        return Json(ScanResponse {
            success: false,
            target,
            scan_type: "error".into(),
            output: msg,
        });
    }

    let mut all_output = String::new();
    let mut scan_types: Vec<&str> = Vec::new();
    let mut overall_success = true;
    let any_option = body.port_scan || body.vuln_scan || body.os_detect || body.dns_query || body.version_detect || body.aggressive_scan || body.net_discover;

    let timing_arg = match body.timing.as_str() {
        "T0" | "T1" | "T2" | "T3" | "T4" | "T5" => format!("-{}", body.timing),
        _ => "-T3".to_string(),
    };
    let t_arg = timing_arg.as_str();

    // в”Ђв”Ђ HiГ§bir Еџey seГ§ilmediyse в†’ Ping в”Ђв”Ђ
    if !any_option {
        scan_types.push("ping");
        let ping_args = if cfg!(target_os = "windows") {
            vec!["-n", "4", &target]
        } else {
            vec!["-c", "4", &target]
        };
        let (ok, out) = run_command("ping", &ping_args);
        overall_success = ok;
        all_output.push_str(&out);
    }

    // в”Ђв”Ђ AДџ KeЕџfi в†’ nmap -sn в”Ђв”Ђ
    if body.net_discover {
        scan_types.push("net_discover");
        if !all_output.is_empty() { all_output.push_str("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n"); }
        let (ok, out) = run_command("nmap", &["-sn", t_arg, "--host-timeout", "60s", &target]);
        overall_success = overall_success && ok;
        all_output.push_str(&out);
    }

    // в”Ђв”Ђ Port Tarama в†’ nmap -F -Pn в”Ђв”Ђ
    if body.port_scan {
        scan_types.push("port_scan");
        if !all_output.is_empty() { all_output.push_str("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n"); }
        let (ok, out) = run_command("nmap", &["-sT", "-F", "-Pn", t_arg, "--host-timeout", "60s", &target]);
        overall_success = overall_success && ok;
        all_output.push_str(&out);
    }

    // в”Ђв”Ђ Zafiyet Analizi в†’ nmap --script vuln -Pn в”Ђв”Ђ
    if body.vuln_scan {
        scan_types.push("vuln_scan");
        if !all_output.is_empty() { all_output.push_str("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n"); }
        let (ok, out) = run_command("nmap", &["-sT", "--script", "vuln", "-Pn", t_arg, "--host-timeout", "60s", &target]);
        overall_success = overall_success && ok;
        all_output.push_str(&out);
    }

    // в”Ђв”Ђ Д°Еџletim Sistemi Tespiti в†’ nmap -O -p 22,80,443 -Pn в”Ђв”Ђ
    if body.os_detect {
        scan_types.push("os_detect");
        if !all_output.is_empty() { all_output.push_str("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n"); }
        let (ok, out) = run_command("nmap", &["-O", "-p", "22,80,443", t_arg, "--host-timeout", "60s", &target]);
        overall_success = overall_success && ok;
        all_output.push_str(&out);
    }

    // в”Ђв”Ђ Servis Versiyon Tespiti в†’ nmap -sV -Pn в”Ђв”Ђ
    if body.version_detect {
        scan_types.push("version_detect");
        if !all_output.is_empty() { all_output.push_str("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n"); }
        let (ok, out) = run_command("nmap", &["-sV", "-Pn", t_arg, "--host-timeout", "60s", &target]);
        overall_success = overall_success && ok;
        all_output.push_str(&out);
    }

    // в”Ђв”Ђ KapsamlД± Agresif Tarama в†’ nmap -A -Pn в”Ђв”Ђ
    if body.aggressive_scan {
        scan_types.push("aggressive_scan");
        if !all_output.is_empty() { all_output.push_str("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n"); }
        let (ok, out) = run_command("nmap", &["-A", "-Pn", t_arg, "--host-timeout", "120s", &target]);
        overall_success = overall_success && ok;
        all_output.push_str(&out);
    }

    // в”Ђв”Ђ DNS Sorgulama в†’ nslookup в”Ђв”Ђ
    if body.dns_query {
        scan_types.push("dns_query");
        if !all_output.is_empty() { all_output.push_str("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n"); }
        let (ok, out) = run_command("nslookup", &[&target]);
        overall_success = overall_success && ok;
        all_output.push_str(&out);
    }

    Json(ScanResponse {
        success: overall_success,
        target,
        scan_type: scan_types.join(","),
        output: all_output,
    })
}

